CREATE TABLE AUTH_NAMES(
AUTH_NAME_PK INT NOT NULL AUTO_INCREMENT,
AUTH_NAME VARCHAR(20) NOT NULL,
PRIMARY KEY(AUTH_NAME_PK)
);
CREATE TABLE STATUS_TABLE (
STATUS_PK INT NOT NULL AUTO_INCREMENT,
STATUS_NAME VARCHAR(20) NOT NULL,
PRIMARY KEY (STATUS_PK)
);
CREATE TABLE DEPARTMENTS(
DEPARTMENT_PK INT NOT NULL AUTO_INCREMENT,
DEPARTMENT_NAME VARCHAR(20) NOT NULL,
PRIMARY KEY (DEPARTMENT_PK)
);
CREATE TABLE USERS(
USER_PK VARCHAR(30),
DEPARTMENT_FK INT,
USER_NAME VARCHAR(20),
USER_PASSWORD VARCHAR(500),
USER_EMAIL VARCHAR(100),
PRIMARY KEY (USER_PK),
FOREIGN KEY (DEPARTMENT_FK) REFERENCES DEPARTMENTS(DEPARTMENT_PK)
);
CREATE TABLE AUTHS(
AUTH_PK INT NOT NULL AUTO_INCREMENT,
USER_FK VARCHAR(30),
AUTH_NAME_FK INT,
PRIMARY KEY (AUTH_PK),
FOREIGN KEY (USER_FK) REFERENCES USERS(USER_PK),
FOREIGN KEY (AUTH_NAME_FK) REFERENCES AUTH_NAMES (AUTH_NAME_PK)
);
CREATE TABLE ATTENDS(
ATTEND_PK INT NOT NULL AUTO_INCREMENT,
STATUS_FK INT,
USER_FK VARCHAR(30),
WORKDATE VARCHAR(30),
PRIMARY KEY (ATTEND_PK),
FOREIGN KEY (STATUS_FK) REFERENCES STATUS_TABLE(STATUS_PK),
FOREIGN KEY (USER_FK) REFERENCES USERS(USER_PK)
);
